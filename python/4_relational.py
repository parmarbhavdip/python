#concept of relational operators
c = int(input("Enter value for c=="))
d = int(input("Enter value for d=="))

result = c == d
print(f"{result} = {c}=={d}")

result = c != d
print(f"{result} = {c}!={d}")

result = c > d
print(f"{result} = {c}>{d}")

result = c < d
print(f"{result} = {c}<{d}")

result = c <= d
print(f"{result} = {c}<={d}")

result = c >= d
print(f"{result} = {c}>={d}")

