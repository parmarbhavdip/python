lit=['jay',102,55.5,'thakar',False,True]
lit1=[0,1,2,3,4,5,6]

# Print complete list
print(lit)

# Print first element of the list
print(lit[0])

# Print elements starting from 1st till 3rd 
print(lit[0:3])

# Print elements starting from 2nd element
print(lit[1:])

# Print list two times
print(lit*2)

# Print concatenated lists
print(lit+lit1)

# print append lists
lit.append(0)
